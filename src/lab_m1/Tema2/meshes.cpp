Mesh* meshes::CreateSnow() {
    srand(static_cast <unsigned> (time(0)));

    std::vector<VertexFormat> vertices;
    std::vector<unsigned int> indices;

    for (int i = 0; i < 300; i = i + 4) {
        for (int j = 0; j < 300; j = j + 4) {
            for (int k = 0; k < 100; k = k + 4) {
                /*float random = (static_cast <float> (rand()) / (static_cast <float> (RAND_MAX)) - 0.5f) * 6;
                if ((int)abs(random * 1000) % 2 == 0) {
                    vertices.push_back({ glm::vec3(i + random, k + random, j + random), glm::vec3(0.93, 0.91, 0.9) });
                }*/

                std::random_device rd;
                std::mt19937 gen(rd());

                // Set the range of the random numbers
                std::uniform_real_distribution<> dis(-2, 2);
                std::uniform_real_distribution<> dis2(0, 1);
                if (dis2(gen) > 0.5f) {
                    vertices.push_back({ glm::vec3(i + dis(gen), k + dis(gen), j + dis(gen)), glm::vec3(0.93, 0.91, 0.9) });
                }
            }
        }
    }

    for (int i = 0; i < vertices.size(); i++) {
        indices.push_back(i);
    }

    Mesh* snow = new Mesh("snow");
    snow->SetDrawMode(GL_POINTS);
    snow->InitFromData(vertices, indices);

    return snow;
}
