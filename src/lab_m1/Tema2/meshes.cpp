Mesh* meshes::CreateTerrain(int size, std::vector<glm::vec3> colors) {
    srand(static_cast <unsigned> (time(0)));

    std::vector<VertexFormat> vertices;
    for (int i = 0; i < size; i++) {
        for (int j = 0; j < size; j++) {
            int random_color = static_cast <float> (rand()) / (static_cast <float> (RAND_MAX)) * (colors.size());
            while (random_color >= colors.size()) {
                random_color = static_cast <float> (rand()) / (static_cast <float> (RAND_MAX)) * (colors.size());
            }
            glm::vec3 color = colors[random_color];
            vertices.push_back({ {i, 0, j}, color});
        }
    }

    std::vector<unsigned int> indices;
    for (int i = 1; i < size; i++) {
        for (int j = 0; j < size - 1; j++) {
            indices.push_back(get_element_by_coordinates(i, j, size));
            indices.push_back(get_element_by_coordinates(i - 1, j + 1, size));
            indices.push_back(get_element_by_coordinates(i - 1, j, size));

            indices.push_back(get_element_by_coordinates(i, j, size));
            indices.push_back(get_element_by_coordinates(i, j + 1, size));
            indices.push_back(get_element_by_coordinates(i - 1, j + 1, size));
        }
    }

    Mesh* terrain = new Mesh("terrain");
    terrain->InitFromData(vertices, indices);

    return terrain;
}


