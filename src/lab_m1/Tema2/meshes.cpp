double cubicBezierPoint(double a0, double a1, double a2, double a3, double t) {
    return pow(1 - t, 3) * a0 + 3 * pow(1 - t, 2) * t * a1 + 3 * (1 - t) * pow(t, 2) * a2 + pow(t, 3) * a3;
}

double quadBezierPoint(double a0, double a1, double a2, double t) {
    return pow(1 - t, 2) * a0 + 2 * (1 - t) * t * a1 + pow(t, 2) * a2;
}

glm::vec3 cubicBezier(glm::vec3 p1, glm::vec3 p2, glm::vec3 p3, glm::vec3 p4, double t) {
    return glm::vec3(
        cubicBezierPoint(p1.x, p2.x, p3.x, p4.x, t),
        cubicBezierPoint(p1.y, p2.y, p3.y, p4.y, t),
        cubicBezierPoint(p1.z, p2.z, p3.z, p4.z, t));
}

glm::vec3 quadBezier(glm::vec3 p1, glm::vec3 p2, glm::vec3 p3, double t) {
    return glm::vec3(
        quadBezierPoint(p1.x, p2.x, p3.x, t),
        quadBezierPoint(p1.y, p2.y, p3.y, t),
        quadBezierPoint(p1.z, p2.z, p3.z, t));
}

std::vector<glm::vec3> BezierCurveNPoints(std::vector<VertexFormat> controls, double detail) {
    std::vector<glm::vec3> renderingPoints;
    std::vector<glm::vec3> controlPoints;

    /* Generate the end and control points. */
    for (int i = 1; i < controls.size() - 1; i += 2) {
        controlPoints.push_back((controls[i - 1].position + controls[i].position) / 2.f);
        controlPoints.push_back(controls[i].position);
        controlPoints.push_back(controls[i + 1].position);

        if (i + 2 < controls.size() - 1) {
            controlPoints.push_back((controls[i + 1].position + controls[i + 2].position) / 2.f);
        }
    }

    /* Generate the detailed points. */
    glm::vec3 a0, a1, a2, a3;
    for (int i = 0; i < controlPoints.size() - 2; i += 4) {
        a0 = controlPoints[i];
        a1 = controlPoints[i + 1];
        a2 = controlPoints[i + 2];

        if (i + 3 > controlPoints.size() - 1) {
            for (double j = 0; j < 1; j += detail) {
                renderingPoints.push_back(quadBezier(a0, a1, a2, j));
            }
        }
        else {
            a3 = controlPoints[i + 3];
            for (double j = 0; j < 1; j += detail) {
                renderingPoints.push_back(cubicBezier(a0, a1, a2, a3, j));
            }
        }
    }

    return renderingPoints;
}


int get_element_by_coordinates(int l, int c, int dim) {
    return l * dim + c;
}


