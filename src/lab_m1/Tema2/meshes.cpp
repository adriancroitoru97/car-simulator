bool ElementIsOnRoad(glm::vec3 pos, Mesh* road) {
    std::vector<VertexFormat> road_axis_vertices = road->vertices;

    for (int i = 0; i < road_axis_vertices.size() - 1; i++) {
        glm::vec3 p1 = road_axis_vertices[i].position;
        glm::vec3 p2 = road_axis_vertices[i + 1].position;

        float u = (pos.x - p1.x) * (p2.x - p1.x) + (pos.z - p1.z) * (p2.z - p1.z);
        float l = glm::length(p2 - p1);
        u = u / (l * l);

        float len;
        if (u < 0) {
            len = glm::length(p1 - pos);
        }
        else if (u > 1) {
            len = glm::length(p2 - pos);
        }
        else {
            float x = p1.x + u * (p2.x - p1.x);
            float y = p1.z + u * (p2.z - p1.z);
            len = glm::length(glm::vec3{ x, 0, y } - pos);
        }

        if (len < 2.8f) {
            return true;
        }
    }

    return false;
}


